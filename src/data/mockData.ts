export function getAllCases(): FullCaseView[] {
  return cases.map((c) => ({
    client: clients.find((cl) => cl.id === c.clientId)!,
    caseData: c,
    treatment: treatments.find((t) => t.caseId === c.id)!,
    providers: providers.filter((p) => p.caseId === c.id),
    referrals: referrals.filter((r) => r.caseId === c.id),
    operational: operationalData.find((o) => o.caseId === c.id)!,
    scores: caseScores[c.id]!,
  }))
}

export function getCaseById(caseId: string): FullCaseView | undefined {
  const c = cases.find((cs) => cs.id === caseId)
  if (!c) return undefined
  return {
    client: clients.find((cl) => cl.id === c.clientId)!,
    caseData: c,
    treatment: treatments.find((t) => t.caseId === c.id)!,
    providers: providers.filter((p) => p.caseId === c.id),
    referrals: referrals.filter((r) => r.caseId === c.id),
    operational: operationalData.find((o) => o.caseId === c.id)!,
    scores: caseScores[c.id]!,
  }
}

export function getTimeline(caseId: string): TimelineEvent[] {
  return (timelines[caseId] || []).sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime())
}

export function daysSince(dateStr?: string): number {
  if (!dateStr) return 999
  const diff = Date.now() - new Date(dateStr).getTime()
  return Math.floor(diff / (1000 * 60 * 60 * 24))
}

export function daysUntil(dateStr?: string): number {
  if (!dateStr) return 999
  const diff = new Date(dateStr).getTime() - Date.now()
  return Math.floor(diff / (1000 * 60 * 60 * 24))
}
